def get_count_of_departments_cleaned_by_robot_vacuum(r, c, d, room_map):
    r=r
    c=c
    total = 0
    stop_state = 0
    while(1):
        if(stop_state == 1):
            break
        if(room_map[r][c] == 0):
            room_map[r][c] = 2#청소완료
            total += 1
        while(1):
            if( (room_map[r-1][c] > 0) & (room_map[r][c-1] > 0) & (room_map[r+1][c] > 0) & (room_map[r][c+1] > 0) ):
                if(d%2==0):
                    r = r+(d-1)*(-1)
                    if(room_map[r][c] == 1):
                        stop_state = 1
                        break
                else:
                    c = c+(d-2)*(-1)
                    if(room_map[r][c] == 1):
                        stop_state = 1
                        break
            else:
                d = (d+1)%4
                if(d%2==0):
                    if(room_map[r+d-1][c] == 0):
                        r = r+d-1
                        break
                else:
                    if(room_map[r][c+d-2] == 0):
                        c = c+d-2
                        break
    return total


